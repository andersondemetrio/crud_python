class Cliente:
    def __init__(self, cpf, nome, telefone_fixo, telefone_celular, sexo, cep, logradouro, numero, complemento, bairro,
                 municipio, estado):

        self.cpf = cpf
        self.nome = nome
        self.telefone_fixo = telefone_fixo
        self.telefone_celular = telefone_celular
        self.sexo = sexo
        self.cep = cep
        self.logradouro = logradouro
        self.numero = numero
        self.complemento = complemento
        self.bairro = bairro
        self.municipio = municipio
        self.estado = estado